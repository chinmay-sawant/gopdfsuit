#!/usr/bin/env python3

import json
import statistics
import sys
import time
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[3]
BENCHMARK_DIR = Path(__file__).resolve().parent
DATA_PATH = BENCHMARK_DIR.parent / "data.json"
OUTPUT_PATH = BENCHMARK_DIR / "output_gopdflib.pdf"

sys.path.insert(0, str(REPO_ROOT / "bindings" / "python"))

from pypdfsuit import Cell, Config, Element, Footer, PDFTemplate, Row, Table, Title, generate_pdf  # noqa: E402


def load_data() -> list[dict]:
    with DATA_PATH.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def build_rows(data: list[dict]) -> list[Row]:
    rows = [
        Row(
            row=[
                Cell(props="Helvetica:10:100:left:1:1:1:1", text="ID", bg_color="#D9EAF7"),
                Cell(props="Helvetica:10:100:left:1:1:1:1", text="Name", bg_color="#D9EAF7"),
                Cell(props="Helvetica:10:100:left:1:1:1:1", text="Email", bg_color="#D9EAF7"),
                Cell(props="Helvetica:10:100:left:1:1:1:1", text="Role", bg_color="#D9EAF7"),
                Cell(props="Helvetica:10:100:left:1:1:1:1", text="Description", bg_color="#D9EAF7"),
            ]
        )
    ]

    for index, record in enumerate(data):
        row_bg = "#F0F0F0" if index % 2 == 0 else None
        rows.append(
            Row(
                row=[
                    Cell(props="Helvetica:10:000:left:1:1:1:1", text=str(record["id"]), bg_color=row_bg),
                    Cell(props="Helvetica:10:000:left:1:1:1:1", text=record["name"], bg_color=row_bg),
                    Cell(
                        props="Helvetica:10:000:left:1:1:1:1",
                        text=record["email"],
                        bg_color=row_bg,
                        wrap=True,
                    ),
                    Cell(props="Helvetica:10:000:left:1:1:1:1", text=record["role"], bg_color=row_bg),
                    Cell(
                        props="Helvetica:10:000:left:1:1:1:1",
                        text=record["desc"],
                        bg_color=row_bg,
                        wrap=True,
                    ),
                ]
            )
        )

    return rows


def build_template(data: list[dict]) -> PDFTemplate:
    return PDFTemplate(
        config=Config(
            page="A4",
            page_alignment=1,
            pdf_title="User Report",
        ),
        title=Title(
            props="Helvetica:16:100:center:0:0:0:0",
            text="User Report",
        ),
        elements=[
            Element(
                type="table",
                table=Table(
                    max_columns=5,
                    column_widths=[15, 30, 50, 35, 60],
                    rows=build_rows(data),
                ),
            )
        ],
        footer=Footer(
            font="Helvetica:8",
            text="Page {page}/{pages} - Generated by GoPDFLib (Python binding)",
        ),
    )


def main() -> None:
    iterations = 10
    data = load_data()
    template = build_template(data)
    timings = []

    print("=== GoPDFLib Data Benchmark ===")
    print(f"Iterations: {iterations}")

    total_start = time.perf_counter()
    for run_index in range(1, iterations + 1):
        start = time.perf_counter()
        pdf_bytes = generate_pdf(template)
        OUTPUT_PATH.write_bytes(pdf_bytes)
        elapsed_ms = (time.perf_counter() - start) * 1000
        timings.append(elapsed_ms)
        print(f"Run {run_index}: {elapsed_ms:.2f} ms")
    total_seconds = time.perf_counter() - total_start

    print()
    print(f"Min: {min(timings):.2f} ms")
    print(f"Avg: {statistics.mean(timings):.2f} ms")
    print(f"Max: {max(timings):.2f} ms")
    print(f"Throughput: {iterations / total_seconds:.2f} ops/sec")
    print(f"Output: {OUTPUT_PATH.name}")


if __name__ == "__main__":
    main()