from fpdf import FPDF
from fpdf.enums import TableCellFillMode
import json
import math
import multiprocessing
import os
import time
from datetime import datetime

DATA_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "data.json")
with open(DATA_PATH, "r") as f:
    data = json.load(f)

ITERATIONS = 10
NUM_WORKERS = min(48, ITERATIONS)


class PDF(FPDF):
    def header(self):
        self.set_font("helvetica", "B", 16)
        self.cell(0, 10, "User Report", align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(5)

    def footer(self):
        self.set_y(-15)
        self.set_font("helvetica", "I", 8)
        self.cell(0, 10, f"Page {self.page_no()}/{{nb}} - Generated by FPDF (PDF 1.7)", align="C")


def run_once(run_index):
    start = time.perf_counter()
    pdf = PDF()

    try:
        pdf.set_pdf_version("1.7")  # Highest PDF version supported by FPDF2
    except Exception:
        pass

    pdf.set_title("User Report")
    pdf.set_author("Benchmark System")
    pdf.set_subject("User data report for benchmarking")
    pdf.set_creator("FPDF2 Benchmark")
    pdf.set_keywords("users, report, benchmark, PDF 1.7")
    pdf.set_creation_date(datetime.now())

    pdf.add_page()
    pdf.set_font("helvetica", size=10)

    col_widths = (15, 30, 50, 35, 60)  # ID, Name, Email, Role, Description

    with pdf.table(
        col_widths=col_widths,
        text_align="LEFT",
        line_height=6,
        cell_fill_mode=TableCellFillMode.ROWS,
        cell_fill_color=(240, 240, 240),
    ) as table:
        pdf.set_font("helvetica", "B", 10)
        header = table.row()
        for h in ["ID", "Name", "Email", "Role", "Description"]:
            header.cell(h)

        pdf.set_font("helvetica", size=10)
        for r in data:
            row = table.row()
            row.cell(str(r["id"]))
            row.cell(r["name"])
            row.cell(r["email"])
            row.cell(r["role"])
            row.cell(r["desc"])

    pdf.output()  # Generate bytes in-memory — avoids file-write races in parallel runs
    return (time.perf_counter() - start) * 1000


if __name__ == "__main__":
    print("=== FPDF2 Data Benchmark ===")
    print(f"Iterations: {ITERATIONS} | Workers: {NUM_WORKERS}")

    total_start = time.perf_counter()
    with multiprocessing.Pool(processes=NUM_WORKERS) as pool:
        all_times = pool.map(run_once, range(1, ITERATIONS + 1))
    total_seconds = time.perf_counter() - total_start

    for i, elapsed in enumerate(all_times, start=1):
        print(f"Run {i}: {elapsed:.2f} ms")

    sorted_times = sorted(all_times)
    p95_idx = max(0, math.ceil(len(sorted_times) * 0.95) - 1)

    print()
    print(f"Min: {min(all_times):.2f} ms")
    print(f"Avg: {sum(all_times)/len(all_times):.2f} ms")
    print(f"P95: {sorted_times[p95_idx]:.2f} ms")
    print(f"Max: {max(all_times):.2f} ms")
    print(f"Throughput: {len(all_times)/total_seconds:.2f} ops/sec")
