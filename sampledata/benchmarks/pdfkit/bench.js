const PDFDocument = require("pdfkit-table");
const fs = require("fs");
const { performance } = require("perf_hooks");

const data = JSON.parse(fs.readFileSync("../data.json", "utf8"));
const iterations = 10;

function buildTableRows() {
    return data.map((record) => [String(record.id), record.name, record.email, record.role, record.desc]);
}

function runOnce() {
    return new Promise((resolve, reject) => {
        const start = performance.now();
        const doc = new PDFDocument({ margin: 30, size: "A4" });
        const stream = fs.createWriteStream("output_pdfkit.pdf");

        stream.on("finish", () => resolve(performance.now() - start));
        stream.on("error", reject);
        doc.pipe(stream);

        doc.fontSize(16).text("User Report", { align: "center" });
        doc.moveDown();

        doc.table(
            {
                title: "",
                headers: ["ID", "Name", "Email", "Role", "Description"],
                rows: buildTableRows(),
            },
            {
                prepareHeader: () => doc.font("Helvetica-Bold").fontSize(10),
                prepareRow: () => doc.font("Helvetica").fontSize(10),
            }
        );

        doc.text("Generated by PDFKit", { align: "center" });
        doc.end();
    });
}

async function main() {
    const timings = [];
    console.log("=== PDFKit Data Benchmark ===");
    console.log(`Iterations: ${iterations}`);

    const totalStart = performance.now();
    for (let runIndex = 1; runIndex <= iterations; runIndex += 1) {
        const elapsedMs = await runOnce();
        timings.push(elapsedMs);
        console.log(`Run ${runIndex}: ${elapsedMs.toFixed(2)} ms`);
    }
    const totalSeconds = (performance.now() - totalStart) / 1000;

    const min = Math.min(...timings);
    const max = Math.max(...timings);
    const avg = timings.reduce((sum, value) => sum + value, 0) / timings.length;

    console.log("");
    console.log(`Min: ${min.toFixed(2)} ms`);
    console.log(`Avg: ${avg.toFixed(2)} ms`);
    console.log(`Max: ${max.toFixed(2)} ms`);
    console.log(`Throughput: ${(timings.length / totalSeconds).toFixed(2)} ops/sec`);
}

main().catch((error) => {
    console.error(error);
    process.exit(1);
});
