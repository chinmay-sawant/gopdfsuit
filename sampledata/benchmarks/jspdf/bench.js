const { jsPDF } = require("jspdf");
const fs = require('fs');
const { performance } = require('perf_hooks');

const data = JSON.parse(fs.readFileSync('../data.json', 'utf8'));
const iterations = 10;

// Attempt to load autoTable
let autoTable = null;
try {
    autoTable = require('jspdf-autotable');
} catch (e) {
    console.log("Could not load jspdf-autotable");
}

let fallbackLogged = false;

function buildBody() {
    return data.map((record) => [
        String(record.id),
        record.name,
        record.email,
        record.role,
        record.desc,
    ]);
}

function drawTextFallback(doc) {
    let y = 30;
    const pageHeight = doc.internal.pageSize.height;

    doc.setFontSize(10);
    for (const r of data) {
        if (y > pageHeight - 20) {
            doc.addPage();
            y = 20;
        }
        const text = `${r.id} | ${r.name} | ${r.email} | ${r.role}`;
        doc.text(text, 14, y);
        y += 10;
    }
}

function runOnce() {
    const start = performance.now();
    const doc = new jsPDF();
    doc.text("User Report", 14, 20);

    const body = buildBody();
    if (doc.autoTable) {
        doc.autoTable({
            startY: 25,
            head: [['ID', 'Name', 'Email', 'Role', 'Description']],
            body,
        });
    } else if (autoTable) {
        if (typeof autoTable === 'function') {
            try {
                autoTable(doc, {
                    startY: 25,
                    head: [['ID', 'Name', 'Email', 'Role', 'Description']],
                    body,
                });
            } catch (e) {
                if (autoTable.default && typeof autoTable.default === 'function') {
                    autoTable.default(doc, {
                        startY: 25,
                        head: [['ID', 'Name', 'Email', 'Role', 'Description']],
                        body,
                    });
                } else {
                    if (!fallbackLogged) {
                        console.log("autoTable function usage failed, falling back to text");
                        fallbackLogged = true;
                    }
                    drawTextFallback(doc);
                }
            }
        } else {
            if (!fallbackLogged) {
                console.log("autoTable is not a function, falling back to text");
                fallbackLogged = true;
            }
            drawTextFallback(doc);
        }
    } else {
        drawTextFallback(doc);
    }

    if (doc.lastAutoTable) {
        doc.text("Generated by jsPDF", 14, doc.lastAutoTable.finalY + 10);
    } else {
        doc.text("Generated by jsPDF", 14, 200);
    }

    const pdfOutput = doc.output('arraybuffer');
    fs.writeFileSync('output_jspdf.pdf', Buffer.from(pdfOutput));
    return performance.now() - start;
}

function main() {
    const timings = [];
    console.log("=== jsPDF Data Benchmark ===");
    console.log(`Iterations: ${iterations}`);

    const totalStart = performance.now();
    for (let runIndex = 1; runIndex <= iterations; runIndex += 1) {
        const elapsedMs = runOnce();
        timings.push(elapsedMs);
        console.log(`Run ${runIndex}: ${elapsedMs.toFixed(2)} ms`);
    }
    const totalSeconds = (performance.now() - totalStart) / 1000;

    const min = Math.min(...timings);
    const max = Math.max(...timings);
    const avg = timings.reduce((sum, value) => sum + value, 0) / timings.length;

    console.log('');
    console.log(`Min: ${min.toFixed(2)} ms`);
    console.log(`Avg: ${avg.toFixed(2)} ms`);
    console.log(`Max: ${max.toFixed(2)} ms`);
    console.log(`Throughput: ${(timings.length / totalSeconds).toFixed(2)} ops/sec`);
}

main();
