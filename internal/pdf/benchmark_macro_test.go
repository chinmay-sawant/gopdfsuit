package pdf

import (
	"fmt"
	"testing"

	"github.com/chinmay-sawant/gopdfsuit/v6/internal/models"
)

func buildSyntheticTableTemplate(rows int) models.PDFTemplate {
	template := models.PDFTemplate{
		Config: pdfAConfig(),
		Title: models.Title{
			Text:  "User Report",
			Props: "Helvetica:24:100:center:0:0:0:0",
		},
		Footer: models.Footer{
			Font: "Helvetica:10:000:center",
			Text: "Generated by gopdfsuit",
		},
	}

	tableRows := make([]models.Row, rows)
	for i := 0; i < rows; i++ {
		id := i + 1
		tableRows[i] = models.Row{
			Row: []models.Cell{
				{Props: "Helvetica:10:000:left:1:1:1:1", Text: fmt.Sprint(id)},
				{Props: "Helvetica:10:000:left:1:1:1:1", Text: fmt.Sprintf("User %d", id)},
				{Props: "Helvetica:10:000:left:1:1:1:1", Text: fmt.Sprintf("user%d@example.com", id)},
				{Props: "Helvetica:10:000:left:1:1:1:1", Text: "Administrator"},
				{Props: "Helvetica:10:000:left:1:1:1:1", Text: "Lorem ipsum dolor sit amet, consectetur adipiscing elit."},
			},
		}
	}

	template.Table = []models.Table{{
		MaxColumns:   5,
		ColumnWidths: []float64{0.5, 1.5, 2, 1.5, 3},
		Rows:         tableRows,
	}}
	return template
}

func buildSyntheticTableTemplateWrapEnabled(rows int) models.PDFTemplate {
	template := buildSyntheticTableTemplate(rows)
	wrap := true
	for i := range template.Table[0].Rows {
		for j := range template.Table[0].Rows[i].Row {
			template.Table[0].Rows[i].Row[j].Wrap = &wrap
		}
	}
	return template
}

func BenchmarkGenerateTemplatePDF(b *testing.B) {
	for _, rows := range []int{2000, 10000, 25000} {
		b.Run(fmt.Sprintf("Rows%d", rows), func(b *testing.B) {
			template := buildSyntheticTableTemplate(rows)
			b.ResetTimer()
			b.ReportAllocs()
			var out []byte
			for i := 0; i < b.N; i++ {
				pdf, err := GenerateTemplatePDF(template)
				if err != nil {
					b.Fatal(err)
				}
				out = pdf
			}
			b.SetBytes(int64(len(out)))
		})
	}
}

